/* Cursor Controls */


#define ESC_CSI_UP 'A'    /* Moves Cursor Up # Lines */
#define ESC_CSI_DOWN 'B'    /* Moves Cursor Down # Lines */
#define ESC_CSI_RIGHT 'C'    /* Moves Cursor Right # Columns */
#define ESC_CSI_LEFT 'D'    /* Moves Cursor Left # Columns */
#define ESC_CSI_END 'E'    /* Moves Cursor To Beginning Of Next Line, # Lines Down */
#define ESC_CSI_ 'F'    /* Moves Cursor To Beginning Of Previous Line, # Lines Up */
#define ESC_CSI_ 'G'    /* Moves Cursor To Column # */